# > <, ==, !=, <=, >=

op_relacional01 = 5 > 3 # maior que

op_relacional02 = 5 < 3 # menor que

op_relacional03 = 5 == 3 # igual a

op_relacional04 = 5 >= 3 # maior ou igual a

op_relacional05 = 5 <= 3 # menor ou igual a

op_relacional06 = 5 != 3 # diferente de

print(f'5 é maior que 3: {op_relacional01}\n'
      f'5 é menor que 3: {op_relacional02}\n'
      f'5 é igual a 3: {op_relacional03}\n'
      f'5 é maior ou igual que 3: {op_relacional04}\n'
      f'5 é menor ou igual que 3: {op_relacional05}\n'
      f'5 é diferente de 3: {op_relacional06}\n')